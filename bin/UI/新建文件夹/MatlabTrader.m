% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

global IDName;
global stockfts;

SecID = get(handles.edit1,'String');
HS = get(handles.popupmenu1,'Value');
if HS == 1
    set(handles.text10,'String',['沪市：',SecID]);
    SecID = [SecID,'.SS'];
else
    set(handles.text10,'String',['深市：',SecID]);
    SecID = [SecID,'.SZ'];
end
if sum(strcmp(IDName(:,1),SecID)) == 0
    errordlg('请检查输入的代码是否有误或者沪深市是否选择错误');
    return;
end

FromDate = [get(handles.edit2,'String'),'/',get(handles.edit3,'String'),'/',get(handles.edit4,'String')];
ToDate = [get(handles.edit5,'String'),'/',get(handles.edit6,'String'),'/',get(handles.edit7,'String')];
Fields = {'Open';'High';'Low';'Close';'Volume'};
stock = fetch(Connect, SecID, Fields, FromDate, ToDate);
stock = stock( end:(-1):1, : );

stockfts = fints(stock(:,1),stock(:,2:end),Fields,'D',SecID);
% Open High Low Close Volume
stockmat = fts2mat(stockfts);

cla(handles.axes1,'reset');
axes(handles.axes1);
MT_candle(fts2mat(stockfts.High),fts2mat(stockfts.Low),...
    fts2mat(stockfts.Close),fts2mat(stockfts.Open),[],stockfts.dates);
hold on;
MA1 = 5;
MA2 = 10;
plot(MA(fts2mat(stockfts.Close),MA1),'k');
plot(MA(fts2mat(stockfts.Close),MA2),'b');

cla(handles.axes2,'reset');
axes(handles.axes2);
set(gca,'XTickLabel',[]);
MT_VolumePlot(stockfts);
set(gca,'XTickLabel',[]);

cla(handles.axes3,'reset');
axes(handles.axes3);
set(gca,'XTickLabel',[]);
MT_MACD(stockfts);
set(gca,'XTickLabel',[]);

% Update handles structure
guidata(hObject, handles);


